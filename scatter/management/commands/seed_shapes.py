class Command(BaseCommand):
    help = 'Generate (x, y) coordinates and store them as Shape.points JSON.'

    def add_arguments(self, parser):
        parser.add_argument('--reset', action='store_true',
            help='Delete all shapes before re-seeding.')
        parser.add_argument('--slug', type=str, default=None,
            help='Only seed one shape, e.g. --slug shark.')

    def handle(self, *args, **options):
        from scatter.data_service import invalidate_cache

        if options['reset']:
            n, _ = Shape.objects.all().delete()
            self.stdout.write(self.style.WARNING(f'Deleted {n} shape(s).'))

        target = options.get('slug')
        defs   = [d for d in SHAPE_DEFS if not target or d['slug'] == target]

        total_pts = 0
        for d in defs:
            shape, _ = Shape.objects.update_or_create(
                slug=d['slug'],
                defaults=dict(
                    label=d['label'], emoji=d['emoji'], color=d['color'],
                    sort_order=d['sort_order'], is_active=True,
                ),
            )
            invalidate_cache(shape.slug)
            x_arr, y_arr = d['gen']()
            pts = [[round(float(x), 4), round(float(y), 4)]
                   for x, y in zip(x_arr, y_arr)]
            shape.points = pts
            shape.save(update_fields=['points'])
            total_pts += len(pts)
            self.stdout.write(f'  ✓  {shape}  →  {len(pts)} points')

        self.stdout.write(self.style.SUCCESS(
            f'\nDone. {len(defs)} shape(s), {total_pts} total points.'
        ))
