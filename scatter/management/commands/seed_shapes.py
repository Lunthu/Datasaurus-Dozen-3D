def gen_star():
    k = 5
    ao = np.linspace(np.pi/2, np.pi/2 + 2*np.pi, k, endpoint=False)
    ai = ao + np.pi/k
    pts = []
    for i in range(k):
        pts.append([np.cos(ao[i]),       np.sin(ao[i])])
        pts.append([np.cos(ai[i]) * 0.4, np.sin(ai[i]) * 0.4])
    pts.append(pts[0])
    arr = np.array(pts)
    tck, _ = splprep([arr[:,0], arr[:,1]], s=0, per=False, k=1)
    t = np.linspace(0, 1, 800)
    xi, yi = splev(t, tck)
    return xi + RNG.normal(0, 0.02, 800), yi + RNG.normal(0, 0.02, 800)


def gen_heart():
    t  = np.linspace(0, 2*np.pi, 800)
    xi = 16 * np.sin(t)**3
    yi = 13*np.cos(t) - 5*np.cos(2*t) - 2*np.cos(3*t) - np.cos(4*t)
    return xi + RNG.normal(0, 0.25, 800), yi + RNG.normal(0, 0.25, 800)


def gen_bullseye():
    pairs = [(1.00,300),(0.72,220),(0.48,150),(0.26,90),(0.08,40)]
    xs, ys = [], []
    for r, n in pairs:
        t = np.linspace(0, 2*np.pi, n)
        xs.append(np.cos(t)*r + RNG.normal(0, 0.02, n))
        ys.append(np.sin(t)*r + RNG.normal(0, 0.02, n))
    return np.concatenate(xs), np.concatenate(ys)


