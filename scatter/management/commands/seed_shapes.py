def gen_skyline():
    buildings = [[0,7,35],[8,9,55],[18,6,42],[25,11,85],[37,5,50],
                 [43,13,105],[57,7,65],[65,8,48],[74,10,78],
                 [85,6,52],[92,8,38],[101,5,60],[107,9,45]]
    xs, ys = [], []
    total = sum(b[1] + b[2]*2 for b in buildings)
    for bx, bw, bh in buildings:
        per = max(14, int(800 * (bw + bh*2) / total))
        s = per // 3
        xs.append(np.full(s, bx)        + RNG.normal(0, 0.3, s))
        ys.append(np.linspace(0, bh, s))
        xs.append(np.linspace(bx, bx+bw, s))
        ys.append(np.full(s, bh)        + RNG.normal(0, 0.3, s))
        xs.append(np.full(s, bx+bw)     + RNG.normal(0, 0.3, s))
        ys.append(np.linspace(0, bh, s))
        nw = per // 4
        xs.append(RNG.uniform(bx+1, bx+bw-1, nw))
        ys.append(RNG.uniform(2, bh-2, nw))
    x, y = np.concatenate(xs), np.concatenate(ys)
    idx  = RNG.choice(len(x), min(800, len(x)), replace=False)
    return x[idx], y[idx]


