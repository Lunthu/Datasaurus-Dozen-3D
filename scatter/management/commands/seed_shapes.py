RNG = np.random.default_rng(42)


# ── Primitives ────────────────────────────────────────────────────────────────

def spline(pts, n, noise=0.6, closed=True):
    arr = np.array(pts, dtype=float)
    k   = min(3, len(arr) - 1)
    try:
        tck, _ = splprep([arr[:, 0], arr[:, 1]], s=0, per=closed, k=k)
        t = np.linspace(0, 1, n)
        xi, yi = splev(t, tck)
    except Exception:
        xi = np.interp(np.linspace(0, len(arr)-1, n), np.arange(len(arr)), arr[:, 0])
        yi = np.interp(np.linspace(0, len(arr)-1, n), np.arange(len(arr)), arr[:, 1])
    return xi + RNG.normal(0, noise, n), yi + RNG.normal(0, noise, n)


def scatter_pts(pts, noise=0.7):
    arr = np.array(pts, dtype=float)
    return (arr[:, 0] + RNG.normal(0, noise, len(arr)),
            arr[:, 1] + RNG.normal(0, noise, len(arr)))


def circle(cx, cy, r, n=80, noise=0.3):
    t = np.linspace(0, 2 * np.pi, n)
    return (np.cos(t) * r + cx + RNG.normal(0, noise, n),
            np.sin(t) * r + cy + RNG.normal(0, noise, n))


def concat(*pairs):
    return (np.concatenate([p[0] for p in pairs]),
            np.concatenate([p[1] for p in pairs]))


def flip_y(xy):
    return xy[0], -xy[1]


